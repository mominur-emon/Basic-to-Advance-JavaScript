var num1 = parseInt(prompt("Enter first number: "));
var num2 = parseInt(prompt("Enter second number: "));

var maximum = Math.max(num1, num2);
document.write(maximum + "<br/>");

var power = Math.pow(num1, num2);
document.write(power + "<br/>");

var squareRoot = Math.sqrt(num1);
document.write(squareRoot + "<br/>");

var absolute = Math.abs(num1);
document.write(absolute + "<br/>");
